// Write a Java program to compare two numbers;

public class hmw03oct8 {
    public static void main(String[] args) {

        int a = 7;
        int b = 10;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
